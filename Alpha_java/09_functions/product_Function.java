public class product_Function {

    public static int product(int a , int b ) {
        return a*b;
        
    }
    public static void main(String[] args) {
        int a =12; 
        int b = 10;
        int prod = product(a, b); 
        System.out.println("the product of a and b is: " +prod);

    }
    
}
